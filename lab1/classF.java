public class classF {
    public static void main(String[] args) throws Exception {
        float x = 23.5f, y = 7.3f;
        System.out.println ("x =" + x);
        System.out.println ("y =" + y);
        System.out.println ("x + y =" + ( x + y) ) ;
        System.out.println ("x - y =" + (x - y) ) ;
        System.out.println ("x * y =" +( x* y) );
        System.out.println ("x / y =" + ( x / y ) );
        System.out.println ("x % y =" + ( x % y ) );
    }
    
}
